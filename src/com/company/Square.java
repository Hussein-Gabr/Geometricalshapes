package com.company;

public class Square {
    private double length;

    public Square() {


    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double perimeter() {

        return length*length;
    }



    public double area() {

        return length*length;
    }



}
